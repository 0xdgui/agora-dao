// Fonction pour détecter le chainId actuel
const getChainId = () => {
  if (typeof window !== 'undefined' && window.ethereum) {
    return parseInt(window.ethereum.chainId, 16);
  }
  return null;
};

// Définir les adresses selon le réseau
export const getContractAddresses = () => {
  const chainId = getChainId();
  
  // Sepolia
  if (chainId === 11155111) {
    return {
      humaToken: sepoliaDeployment.humaToken,
      vault: sepoliaDeployment.vault,
      governance: sepoliaDeployment.governance,
      network: 'sepolia'
    };
  }
  
  // Hardhat/localhost (par défaut)
  return {
    humaToken: localDeployment.humaToken,
    vault: localDeployment.vault,
    governance: localDeployment.governance,
    network: 'localhost'
  };
};

